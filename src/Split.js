/**
 * Split.
 *
 * @param {number} k
 * @param {number} xBegin
 * @param {number} xEnd
 * @param {number} distanceLeft
 * @param {number} distance
 */
export default function Split(k, xBegin, xEnd, distanceLeft, distance) {
	this.k = k;
	this.xBegin = xBegin;
	this.xEnd = xEnd;
	this.distanceLeft = distanceLeft;
	this.distance = distance;
}
